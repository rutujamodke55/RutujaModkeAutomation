package ArrayDemo;

public class MultiDimensionalArray {
    public static void main(String[] args) {
        int x[][]={{11,22,33},{44,55,66},{77,88,99}};
          for (int i=0; i<x.length; i++){
            for(int j=0; j<x.length; j++) {
                System.out.print(x[i][j] + " ");
            }
            System.out.println();
            }
          }
    }

